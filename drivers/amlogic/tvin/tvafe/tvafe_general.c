			W_APB_BIT(ADC_REG_06, 0, ENPGA_BIT, ENPGA_WID);
			W_APB_BIT(ADC_REG_17, pin-TVAFE_ADC_PIN_A_0,
						INMUXA_BIT, INMUXA_WID);
			ret = TVAFE_ADC_CH_A;

