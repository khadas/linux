/*
 * compare calibration value (calvalue) with standard value (stdvalue)
 */
unsigned char tvafe_compare_cal_value(unsigned short calvalue,
					unsigned short stdvalue)
{
	unsigned char ret = 0;
	if (calvalue > stdvalue) {

		if ((calvalue - stdvalue) > threshold_value) {

			ret = 1;
			return ret;
		}

	} else if ((stdvalue - calvalue) > threshold_value) {

		ret = 1;
		return ret;
	}
	return ret;

}
/*
 * if adc_cal value is not valid,then adc_cal value will been replaced
 */
void tvafe_adjust_cal_value(struct tvafe_adc_cal_s *para,
						bool iscomponent)
{
	unsigned char flag = 0;
	struct tvafe_adc_cal_s *stdvaluep =
		iscomponent ? (&cal_std_value_component) : (&cal_std_value_vga);

	flag |= tvafe_compare_cal_value(para->a_analog_clamp,
		stdvaluep->a_analog_clamp);
	flag |= tvafe_compare_cal_value(para->a_analog_gain,
		stdvaluep->a_analog_gain);

	flag |= tvafe_compare_cal_value(para->b_analog_clamp,
		stdvaluep->b_analog_clamp);
	flag |= tvafe_compare_cal_value(para->b_analog_gain,
		stdvaluep->b_analog_gain);

	flag |= tvafe_compare_cal_value(para->c_analog_clamp,
		stdvaluep->c_analog_clamp);
	flag |= tvafe_compare_cal_value(para->c_analog_gain,
		stdvaluep->c_analog_gain);

	if (flag)
		tvafe_reset_cal_value(para, stdvaluep);

}
