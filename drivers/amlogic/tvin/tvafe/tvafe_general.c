		W_HIU_REG(HHI_VAFE_CLKXTALIN_CNTL, 0);
		W_HIU_REG(HHI_VAFE_CLKOSCIN_CNTL, 0);
		W_HIU_REG(HHI_VAFE_CLKIN_CNTL, 0);
		W_HIU_REG(HHI_VAFE_CLKPI_CNTL, 0);
